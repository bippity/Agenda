
class Category 
{
	public int ID;
	public String Name, TabID;
	
	public Category(int id, String name, String tabID)
	{
		ID = id;
		Name = name;
		TabID = tabID;
	}
}

class Tab
{
	public int ID;
	public String Info, Date;
	
	public Tab(int id, String info, String date)
	{
		ID = id;
		Info = info;
		Date = date;
	}
}
